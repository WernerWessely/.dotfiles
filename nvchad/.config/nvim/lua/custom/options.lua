-- set 4 space for indent and tabs
vim.opt.tabstop = 4
vim.opt.softtabstop = 4
vim.opt.expandtab = true
vim.opt.shiftwidth = 0

-- enable mouse for all modes
vim.opt.mouse = "a"

-- keep cursor 5 columns from top/bottom
vim.opt.scrolloff = 5

-- add line over there -->
vim.opt.colorcolumn = "80"

-- use hybrid line numbers
vim.opt.number = true
vim.opt.relativenumber = true

-- ignore case for search, except when seach string has upper case chars
vim.opt.ignorecase = true
vim.opt.smartcase = true

-- add cursor line
vim.opt.cursorline = true
